function quote(value: string | null | undefined): string {
  if (value === null || value === undefined) return 'NULL'
  return `'${value.replaceAll("'", "''")}'`
}

function num(value: number | null | undefined): string {
  if (value === null || value === undefined) return 'NULL'
  return String(value)
}

