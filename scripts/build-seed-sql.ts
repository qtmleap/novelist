// --- Cities ---
for (const c of CITIES) {
  const region = regionOfPrefecture(c.prefecture)
  if (!region) throw new Error(`unknown region for ${c.prefecture}`)
  const regionCode = regionCodeFor[region]
  lines.push(
    `INSERT INTO City (code, name_ja, prefecture, lat, lng, airport_iata, hotel_tier, region_code) VALUES (${quote(c.code)}, ${quote(c.name_ja)}, ${quote(c.prefecture)}, ${num(c.lat)}, ${num(c.lng)}, ${c.airport_iata ? quote(c.airport_iata) : 'NULL'}, 0, ${quote(regionCode)});`
  )
}

// --- Rail stations + Shinkansen profiles ---
for (const s of SHINKANSEN_STATIONS) {
  const op = CORRIDOR_TO_OPERATOR[s.corridor] ?? 'jr_east'
  lines.push(
    `INSERT INTO RailStation (code, name_ja, operator_code, lat, lng) VALUES (${quote(s.code)}, ${quote(s.name_ja)}, ${quote(op)}, NULL, NULL);`
  )
  lines.push(
    `INSERT INTO ShinkansenStationProfile (station_code, corridor, km_from_origin) VALUES (${quote(s.code)}, ${quote(s.corridor)}, ${num(s.km_from_origin)});`
  )
}

// --- City ↔ Station ---
for (const c of CITIES) {
  for (const sc of c.shinkansen_station_codes) {
    lines.push(`INSERT INTO CityRailStation (city_code, station_code) VALUES (${quote(c.code)}, ${quote(sc)});`)
  }
}

